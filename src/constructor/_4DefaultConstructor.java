package constructor;

public class _4DefaultConstructor {

	int num;
	boolean val;
	String s;
	char c;

	public static void main(String[] args) {

		_4DefaultConstructor ex = new _4DefaultConstructor();
		System.out.println(ex.num);
		System.out.println(ex.val);
		System.out.println(ex.s);
		System.out.println(ex.c);
	}

}
